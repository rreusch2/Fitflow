        var text = "ðŸ‹ï¸â€â™‚ï¸ \(workout.title)\n\n"
        text += "ðŸ’ª Muscle Groups: \(workout.muscleGroups.map { $0.displayName }.joined(separator: ", "))\n"
        text += "â±ï¸ Duration: \(workout.duration.displayName)\n"
        text += "ðŸ”¥ Difficulty: \(workout.difficulty.displayName)\n\n"
        text += "ðŸ“‹ Exercises:\n"
        
        for (index, exercise) in workout.exercises.enumerated() {
            text += "\n\(index + 1). \(exercise.name)\n"
            text += "   Sets: \(exercise.sets) | Reps: \(exercise.reps) | Rest: \(exercise.rest)\n"
            if !exercise.notes.isEmpty {
                text += "   ðŸ’¡ \(exercise.notes)\n"
            }
        }
        
        text += "\n\nâœ¨ Generated by Flowmate AI"
        return text
    }
}

// MARK: - Supporting Views

struct OverviewItem: View {
    let title: String
    let value: String
    let icon: String
    @EnvironmentObject var themeProvider: ThemeProvider
    
    var body: some View {
        VStack(spacing: 8) {
            Image(systemName: icon)
                .font(.system(size: 20, weight: .medium))
                .foregroundColor(themeProvider.theme.accent)
            
            VStack(spacing: 4) {
                Text(value)
                    .font(.system(size: 18, weight: .bold))
                    .foregroundColor(themeProvider.theme.textPrimary)
                
                Text(title)
                    .font(.system(size: 12, weight: .medium))
                    .foregroundColor(themeProvider.theme.textSecondary)
            }
        }
        .frame(maxWidth: .infinity)
    }
}

struct ExerciseCard: View {
    let exercise: AIExercise
    let index: Int
    let isCompleted: Bool
    let onToggle: () -> Void
    @EnvironmentObject var themeProvider: ThemeProvider
    
    var body: some View {
        HStack(spacing: 16) {
            // Exercise Number/Checkmark
            Button(action: onToggle) {
                ZStack {
                    Circle()
                        .fill(isCompleted ? Color.green : themeProvider.theme.accent.opacity(0.1))
                        .frame(width: 32, height: 32)
                    
                    if isCompleted {
                        Image(systemName: "checkmark")
                            .font(.system(size: 14, weight: .bold))
                            .foregroundColor(.white)
                    } else {
                        Text("\(index)")
                            .font(.system(size: 14, weight: .bold))
                            .foregroundColor(themeProvider.theme.accent)
                    }
                }
            }
            
            // Exercise Details
            VStack(alignment: .leading, spacing: 8) {
                Text(exercise.name)
                    .font(.system(size: 18, weight: .semibold))
                    .foregroundColor(themeProvider.theme.textPrimary)
                    .strikethrough(isCompleted)
                
                HStack(spacing: 16) {
                    ExerciseDetail(label: "Sets", value: "\(exercise.sets)")
                    ExerciseDetail(label: "Reps", value: exercise.reps)
                    ExerciseDetail(label: "Rest", value: exercise.rest)
                }
                
                if !exercise.notes.isEmpty {
                    Text(exercise.notes)
                        .font(.system(size: 12, weight: .medium))
                        .foregroundColor(themeProvider.theme.textSecondary)
                        .padding(.horizontal, 8)
                        .padding(.vertical, 4)
                        .background(
                            RoundedRectangle(cornerRadius: 6)
                                .fill(themeProvider.theme.accent.opacity(0.1))
                        )
                }
            }
            
            Spacer()
        }
        .padding(16)
        .background(
            RoundedRectangle(cornerRadius: 12)
                .fill(themeProvider.theme.backgroundSecondary)
                .overlay(
                    RoundedRectangle(cornerRadius: 12)
                        .stroke(
                            isCompleted ? Color.green.opacity(0.3) : Color.gray.opacity(0.2),
                            lineWidth: 1
                        )
                )
                .opacity(isCompleted ? 0.7 : 1.0)
        )
        .animation(.spring(response: 0.3, dampingFraction: 0.7), value: isCompleted)
    }
}

struct ExerciseDetail: View {
    let label: String
    let value: String
    @EnvironmentObject var themeProvider: ThemeProvider
    
    var body: some View {
        VStack(spacing: 2) {
            Text(label)
                .font(.system(size: 10, weight: .medium))
                .foregroundColor(themeProvider.theme.textSecondary)
            
            Text(value)
                .font(.system(size: 12, weight: .semibold))
                .foregroundColor(themeProvider.theme.textPrimary)
        }
    }
}

struct ShareSheet: UIViewControllerRepresentable {
    let items: [Any]
    
    func makeUIViewController(context: Context) -> UIActivityViewController {
        UIActivityViewController(activityItems: items, applicationActivities: nil)
    }
    
    func updateUIViewController(_ uiViewController: UIActivityViewController, context: Context) {}
}

